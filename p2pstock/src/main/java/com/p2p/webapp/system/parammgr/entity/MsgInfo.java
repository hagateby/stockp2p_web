package com.p2p.webapp.system.parammgr.entity;

public class MsgInfo {
	
	private String msg_id;
	private String msg_code;
	private String msg_typ;
	private String msg_comment;
	
	public String getMsg_id() {
		return msg_id;
	}
	public void setMsg_id(String msgId) {
		msg_id = msgId;
	}
	public String getMsg_code() {
		return msg_code;
	}
	public void setMsg_code(String msgCode) {
		msg_code = msgCode;
	}
	public String getMsg_typ() {
		return msg_typ;
	}
	public void setMsg_typ(String msgTyp) {
		msg_typ = msgTyp;
	}
	public String getMsg_comment() {
		return msg_comment;
	}
	public void setMsg_comment(String msgComment) {
		msg_comment = msgComment;
	}
}
